public class PersonTask1 {
    private String name;
    private String Egn;

    public PersonTask1(String name, String Egn) {
        this.name = name;
        this.Egn = Egn;
    }

    public String GetName() {
        return name;
    }

    public void SetName(String name) {
        this.name = name;
    }

    public String GetEgn() {
        return Egn;
    }

    public void SetEgn(String Egn) {
        this.Egn = Egn;
    }
}
